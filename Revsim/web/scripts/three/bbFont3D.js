function get3DCharObj(ch) {
	switch(ch) {
	  case '(': return leftParen3D;
	  case ')': return rightParen3D;
	  case ',': return comma3D;
	  case '0': return zero3D;
	  case '1': return one3D;
	  case '2': return two3D;
	  case '3': return three3D;
	  case '4': return four3D;
	  case '5': return five3D;
	  case '6': return six3D;
	  case '7': return seven3D;
	  case '8': return eight3D;
	  case '9': return nine3D;
	  default : return null;
	}
}
